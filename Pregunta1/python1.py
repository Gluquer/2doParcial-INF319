#Suma
def Suma(x, y):
    return x + y
# Resta
def Resta(x, y):
    return x - y
# Multiplica
def Multiplicar(x, y):
    return x * y
# Divisi√≥n
def Dividir(x, y):
    return x / y
def Calculadora(x, y):
    print("Suma ",Suma(x, y))
    print("Resta ",Resta(x, y))
    print("Mul ",Multiplicar(x, y))
    print("Div ",Dividir(x, y))

print(Calculadora(8,2))